public class While1 {
        
    /**
     * Creates a new instance of <code>While1</code>.
     */
    public While1() {
    }
    
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int count = 1; //use this to keep track of loop
        while (count < 21 ){
        	System.out.println(count);
        	count++; //increment by 1
        }
    }
}
