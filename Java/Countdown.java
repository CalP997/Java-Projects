public class Countdown {
        
    /**
     * Creates a new instance of <code>Countdown</code>.
     */
    public Countdown() {
    }
    
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
       for ( int i = 11; i > 0 ; i-- ) {
       	System.out.println(i);
       }
    }
}
